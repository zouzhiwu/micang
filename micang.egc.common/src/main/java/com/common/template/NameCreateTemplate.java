package com.common.template;

public class NameCreateTemplate   extends TemplateBase{
	private String nick;
	
	private String fastName;
	
	private String lastName;

	public NameCreateTemplate(Integer id,String nick, String fastName, String lastName) {
		super.setId(id);
		this.nick = nick;
		this.fastName = fastName;
		this.lastName = lastName;
	}

	public String getFastName() {
		return fastName;
	}

	public String getLastName() {
		return lastName;
	}

	public String getNick() {
		return nick;
	}
	
}
