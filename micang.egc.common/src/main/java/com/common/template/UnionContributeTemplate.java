package com.common.template;

public class UnionContributeTemplate {
	
	private Integer id;
	private String name;
	private Byte type;
	private Integer itemId;
	private Integer num;
	private Integer build; 
	private Integer exp;
	private Integer currency;
	
	
	
	
	public UnionContributeTemplate(Integer id, String name, Byte type, Integer itemId, Integer num, Integer build,
			Integer exp, Integer currency) {
		super();
		this.id = id;
		this.name = name;
		this.type = type;
		this.itemId = itemId;
		this.num = num;
		this.build = build;
		this.exp = exp;
		this.currency = currency;
	}




	public Integer getId() {
		return id;
	}




	public String getName() {
		return name;
	}




	public Byte getType() {
		return type;
	}




	public Integer getItemId() {
		return itemId;
	}




	public Integer getNum() {
		return num;
	}




	public Integer getBuild() {
		return build;
	}




	public Integer getExp() {
		return exp;
	}




	public Integer getCurrency() {
		return currency;
	}
	
}
