package com.common.template;

public class RobotTemplate  extends TemplateBase {
	private Integer upperRankingLimit   ;
	private Integer lowerRankingLimit   ;
	private Short upperMemberGrade      ;
	private Short lowerMemberGrade      ;
	private Short upperMemberLv         ;
	private Short lowerMemberLv         ;
	private Short upperHeroProficiencyLv; 
	private Short lowerHeroProficiencyLv;
	private Byte upperMemberStar;
	private Byte lowerMemberStar;
	
	public RobotTemplate(Integer id,Integer upperRankingLimit, Integer lowerRankingLimit, Short upperMemberGrade,
			Short lowerMemberGrade, Short upperMemberLv, Short lowerMemberLv, Short upperHeroProficiencyLv,
			Short lowerHeroProficiencyLv,Byte upperMemberStar,Byte lowerMemberStar) {
		super.setId(id);
		this.upperRankingLimit = upperRankingLimit;
		this.lowerRankingLimit = lowerRankingLimit;
		this.upperMemberGrade = upperMemberGrade;
		this.lowerMemberGrade = lowerMemberGrade;
		this.upperMemberLv = upperMemberLv;
		this.lowerMemberLv = lowerMemberLv;
		this.upperHeroProficiencyLv = upperHeroProficiencyLv;
		this.lowerHeroProficiencyLv = lowerHeroProficiencyLv;
		this.upperMemberStar = upperMemberStar;
		this.lowerMemberStar = lowerMemberStar;
	}
	public Integer getUpperRankingLimit() {
		return upperRankingLimit;
	}
	public Integer getLowerRankingLimit() {
		return lowerRankingLimit;
	}
	public Short getUpperMemberGrade() {
		return upperMemberGrade;
	}
	public Short getLowerMemberGrade() {
		return lowerMemberGrade;
	}
	public Short getUpperMemberLv() {
		return upperMemberLv;
	}
	public Short getLowerMemberLv() {
		return lowerMemberLv;
	}
	public Short getUpperHeroProficiencyLv() {
		return upperHeroProficiencyLv;
	}
	public Short getLowerHeroProficiencyLv() {
		return lowerHeroProficiencyLv;
	}
	public Byte getUpperMemberStar() {
		return upperMemberStar;
	}
	public Byte getLowerMemberStar() {
		return lowerMemberStar;
	}
}
