package com.common.template;

public class StageDropTemplate extends TemplateBase {
	
	private String dropGroup;
	private String dropMinNum;
	private String dropMaxNum;
	private String randDropGroup;
	private String randDropRate;
	private String randDropMinNum;
	private String randDropMaxNum;
	
	public StageDropTemplate(Integer id, String dropGroup, String dropMinNum, String dropMaxNum, String randDropGroup,
			String randDropRate, String randDropMinNum, String randDropMaxNum) {
		super.setId(id);
		super.setName("name" + id);
		this.dropGroup = dropGroup;
		this.dropMinNum = dropMinNum;
		this.dropMaxNum = dropMaxNum;
		this.randDropGroup = randDropGroup;
		this.randDropRate = randDropRate;
		this.randDropMinNum = randDropMinNum;
		this.randDropMaxNum = randDropMaxNum;
	}
	
	public String getDropGroup() {
		return dropGroup;
	}
	public String getDropMinNum() {
		return dropMinNum;
	}
	public String getDropMaxNum() {
		return dropMaxNum;
	}
	public String getRandDropGroup() {
		return randDropGroup;
	}
	public String getRandDropRate() {
		return randDropRate;
	}
	public String getRandDropMinNum() {
		return randDropMinNum;
	}
	public String getRandDropMaxNum() {
		return randDropMaxNum;
	}

}
