package com.common.template;

public class UnionLevelTemplate {

	private Integer id;
	private Integer lv;
	private Integer exp;
	private Integer totalExp;
	private Integer num;

	
	
	public UnionLevelTemplate(Integer id, Integer lv, Integer exp, Integer totalExp, Integer num) {
		super();
		this.id = id;
		this.lv = lv;
		this.exp = exp;
		this.totalExp = totalExp;
		this.num = num;
	}



	public Integer getId() {
		return id;
	}



	public Integer getLv() {
		return lv;
	}



	public Integer getExp() {
		return exp;
	}



	public Integer getTotalExp() {
		return totalExp;
	}



	public Integer getNum() {
		return num;
	}


}
