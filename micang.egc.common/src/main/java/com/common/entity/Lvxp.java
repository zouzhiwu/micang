package com.common.entity;

public class Lvxp {
	private Integer level;
	private Integer xp;
	
	public Lvxp(Integer level, Integer xp) {
		this.level = level;
		this.xp = xp;
	}
	public Integer getLevel() {
		return level;
	}
	public void setLevel(Integer level) {
		this.level = level;
	}
	public Integer getXp() {
		return xp;
	}
	public void setXp(Integer xp) {
		this.xp = xp;
	}
	
	@Override
	public String toString() {
		return String.format("level=%d xp=%d", level, xp);
	}
}
