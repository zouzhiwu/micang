public class CoinItemCountBean {
	
	private Integer coinItemId;
	private Integer count;
	
	public CoinItemCountBean(Integer coinItemId, Integer count) {
		super();
		this.coinItemId = coinItemId;
		this.count = count;
	}
	public Integer getCoinItemId() {
		return coinItemId;
	}
	public void setCoinItemId(Integer coinItemId) {
		this.coinItemId = coinItemId;
	}
	public Integer getCount() {
		return count;
	}
	public void setCount(Integer count) {
		this.count = count;
	}
}
