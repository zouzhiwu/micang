package com.game.entity.vo;

public class Friend {
	/**好友id**/
	private Long accountId;	
	
	 /**头像**/
	private String logo;
	
	 /**昵称**/
	private String nickName;
	
	/**签名**/
	private String sign;
	
	/**等级**/
	private short lv;
	
	 /**战力**/
	private int fighting;
	
	 /**在线状态**/
	private Boolean start;
	
	 /**下线时间**/
	private Long outTime;
	
	 /**体力赠送状态 true 送过 false-没送过**/
	private Boolean physicalGive;
	
	 /**体力给予状态 true 给了 false 没给**/
	private Boolean physicalGet;
	
	public Long getAccountId() {
		return accountId;
	}
	public void setAccountId(Long accountId) {
		this.accountId = accountId;
	}
	public String getLogo() {
		return logo;
	}
	public void setLogo(String logo) {
		this.logo = logo;
	}
	public String getNickName() {
		return nickName;
	}
	public void setNickName(String nickName) {
		this.nickName = nickName;
	}
	public String getSign() {
		return sign;
	}
	public void setSign(String sign) {
		this.sign = sign;
	}
	public short getLv() {
		return lv;
	}
	public void setLv(short lv) {
		this.lv = lv;
	}
	public int getFighting() {
		return fighting;
	}
	public void setFighting(int fighting) {
		this.fighting = fighting;
	}
	public Boolean getStart() {
		return start;
	}
	public void setStart(Boolean start) {
		this.start = start;
	}
	public Long getOutTime() {
		return outTime;
	}
	public void setOutTime(Long outTime) {
		this.outTime = outTime;
	}
	public Boolean getPhysicalGive() {
		return physicalGive;
	}
	public void setPhysicalGive(Boolean physicalGive) {
		this.physicalGive = physicalGive;
	}
	public Boolean getPhysicalGet() {
		return physicalGet;
	}
	public void setPhysicalGet(Boolean physicalGet) {
		this.physicalGet = physicalGet;
	}
}
